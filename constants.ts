export const getVerdictStyle = (verdict: RabbitVerdict | string) => {
  if (verdict === RabbitVerdict.RECOMMEND || verdict === '是' || verdict === 'Y' || verdict === 'TRUE') {
    return {
      color: 'text-pink-600',
      bg: 'bg-pink-100',
      border: 'border-pink-300',
      markerColor: '#ec4899', 
      label: '😍'
    };
  }
  if (verdict === RabbitVerdict.NOT_RECOMMENDED || verdict === '否' || verdict === 'N' || verdict === 'FALSE') {
    return {
      color: 'text-gray-500',
      bg: 'bg-gray-200',
      border: 'border-gray-300',
      markerColor: '#9ca3af', 
      label: '🙈'
    };
  }
  return {
    color: 'text-amber-800',
    bg: 'bg-[#e6d7c3]', 
    border: 'border-[#d4c5b0]',
    markerColor: '#d6c09c', 
    label: '😎'
  };
};

